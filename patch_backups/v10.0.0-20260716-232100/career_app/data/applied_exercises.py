APPLIED_PATTERN = re.compile(
    r"^Complete Applied Lab (\d{2}):",
    re.IGNORECASE,
)

def exercise_number_for_label(label):
    text = str(label or "").strip()
    match = APPLIED_PATTERN.match(text)
    if match:
        number = int(match.group(1))
        return number if number in APPLIED_EXERCISES else None
    for number, item in APPLIED_EXERCISES.items():
        if text == item["label"] or text in item.get("aliases", []):
            return number
    return None

def exercise_for_label(label):
    number = exercise_number_for_label(label)
    return APPLIED_EXERCISES.get(number) if number is not None else None

def exercise_source(label):
    number = exercise_number_for_label(label)
    if number is None:
        return None
    item = APPLIED_EXERCISES[number]
    return (
        f"Applied Labs • {item['category']} • Lab {number:02d} • "
        f"practice/applied/exercises/{item['slug']}"
    )
