def _ensure_notebook_kernel(path: Path) -> bool:
    """Point an existing managed notebook at the dedicated app environment.

    Only notebook metadata is changed. Learner SQL, Markdown, outputs, and execution
    history are preserved exactly as they are.
    """
    try:
        payload = json.loads(path.read_text(encoding="utf-8"))
    except (OSError, UnicodeError, json.JSONDecodeError):
        return False
    if not isinstance(payload, dict):
        return False

    metadata = payload.setdefault("metadata", {})
    expected = {
        "display_name": NOTEBOOK_KERNEL_DISPLAY_NAME,
        "language": "python",
        "name": NOTEBOOK_KERNEL_NAME,
    }
    changed = metadata.get("kernelspec") != expected
    if changed:
        metadata["kernelspec"] = expected
        path.write_text(
            json.dumps(payload, indent=2, ensure_ascii=False) + "\n",
            encoding="utf-8",
        )
    return changed


def _notebook_can_refresh(path: Path) -> bool:
    try:
        payload = json.loads(path.read_text(encoding="utf-8"))
    except (OSError, UnicodeError, json.JSONDecodeError):
        return False
    metadata = payload.get("metadata") or {}
    if not metadata.get("dcaManaged"):
        return False
    cells = payload.get("cells") or []
    if _notebook_source_hash(cells) != str(metadata.get("dcaSourceHash") or ""):
        return False
    for cell in cells:
        if cell.get("cell_type") == "code":
            if cell.get("execution_count") is not None or cell.get("outputs"):
                return False
    return True


def _old_sql_body(source: str) -> tuple[str, str] | None:
    match = re.search(
        r'^\s*([A-Za-z_][A-Za-z0-9_]*)\s*=\s*' + '"' * 3 + r'(.*?)' + '"' * 3,
        str(source or ""),
        flags=re.S,
    )
    if not match:
        return None
    name = match.group(1)
    sql = match.group(2).strip()
    if "DCA TODO" in sql or not re.search(r"\b(SELECT|WITH|CREATE|DESCRIBE|SHOW)\b", sql, re.I):
        return name, ""
    return name, sql


def _old_query_section(variable_name: str) -> str | None:
    name = str(variable_name or "").casefold()
    if name.startswith("duplicate_"):
        return "primary_keys"
    if name.startswith("orphan_"):
        return "orphan_keys"
    if name.startswith("cardinality_"):
        return "cardinality"
    if name.startswith("project_consistency"):
        return "consistency"
    return None


def _archive_notebook(plan: ProjectDataPlan, path: Path) -> Path:
    archive = _archive_path(plan, path)
    archive.parent.mkdir(parents=True, exist_ok=True)
    shutil.copy2(path, archive)
    return archive


def _relationship_notebook_recognized(payload: dict[str, Any]) -> bool:
    metadata = payload.get("metadata") or {}
    if metadata.get("dcaTemplate") == "relationship-validation":
        return True
    for cell in payload.get("cells") or []:
        source = str(cell.get("source") or "")
        if NOTEBOOK_HEADER in source or "Validate Table Relationships" in source:
            return True
        if "%load_ext sql" in source and "project_sources.yaml" in source:
            return True
    return False


def _sql_work_body(source: str) -> str:
    text = str(source or "").replace("\r\n", "\n").strip()
    if text.startswith("%%sql"):
        text = text[len("%%sql"):].lstrip()
    meaningful = [
        line
        for line in text.splitlines()
        if line.strip() and not line.lstrip().startswith("--")
    ]
    return text if meaningful else ""


def _legacy_interpretation_defaults() -> dict[str, str]:
    return {
        "primary_keys": '''### What did the uniqueness checks show?

Record any duplicated identifiers, explain whether they are expected, and note what
must be cleaned or documented before those keys are used in joins.

_Write your findings here._''',
        "orphan_keys": '''### What did the missing-parent checks show?

Record any orphaned records, describe the likely cause, and explain whether the issue
needs cleaning, exclusion, or a documented exception.

_Write your findings here._''',
        "cardinality": '''### What did the row-count comparisons show?

State which joins preserved the expected grain and explain any unexpected increases or
decreases in row counts.

_Write your findings here._''',
        "consistency": '''### What did the additional checks show?

Describe the business rule you tested, why it matters, and what action the result
suggests.

_Write your findings here._''',
    }


def _interpretation_work(section: str, source: str) -> str:
    text = str(source or "").replace("\r\n", "\n").strip()
    minimal = "### Interpretation\n\n_Write your findings here._"
    legacy = _legacy_interpretation_defaults().get(section, "")
    if text in {"", minimal, legacy}:
        return ""
    return text


def _final_conclusion_work(source: str) -> str:
    text = str(source or "").replace("\r\n", "\n").strip()
    if not text:
        return ""
    legacy_placeholders = (
        "_Summarize the uniqueness results._",
        "_Summarize the missing-parent results._",
        "_Summarize whether the tested joins preserved the expected row counts._",
        "_Document intentional exceptions, source-data problems, and required cleanup._",
        "_State which relationships are safe to use, which are not, and what must happen before analysis continues._",
    )
    if all(value in text for value in legacy_placeholders):
        return ""
    current_placeholders = (
        "_Write your conclusion here._",
        "_State whether analysis can continue with these relationships._",
    )
    if all(value in text for value in current_placeholders):
        return ""
    return text


def _upgrade_notebook_to_current(path: Path, plan: ProjectDataPlan) -> bool:
    try:
        old_payload = json.loads(path.read_text(encoding="utf-8"))
    except (OSError, UnicodeError, json.JSONDecodeError):
        return False
    if not isinstance(old_payload, dict) or not _relationship_notebook_recognized(old_payload):
        return False

    metadata = old_payload.get("metadata") or {}
    version = int(metadata.get("dcaTemplateVersion") or 0)
    if metadata.get("dcaManaged") and version >= RELATIONSHIP_NOTEBOOK_TEMPLATE_VERSION:
        return False

    section_map = {
        "baseline": "baseline",
        "primary_keys": "primary_keys",
        "orphan_keys": "foreign_keys",
        "foreign_keys": "foreign_keys",
        "cardinality": "cardinality",
        "consistency": "consistency",
    }
    migrated_sql: dict[str, list[str]] = {}
    migrated_interpretations: dict[str, str] = {}
    migrated_final = ""
    has_outputs = False

    for cell in old_payload.get("cells") or []:
        cell_type = str(cell.get("cell_type") or "")
        cell_metadata = cell.get("metadata") or {}
        role = str(cell_metadata.get("dcaRole") or "")
        old_section = str(cell_metadata.get("dcaSection") or "")
        new_section = section_map.get(old_section, old_section)

        if cell_type == "code":
            has_outputs = has_outputs or cell.get("execution_count") is not None or bool(cell.get("outputs"))
            if role == "learner-sql" and new_section:
                body = _sql_work_body(str(cell.get("source") or ""))
                if body:
                    migrated_sql.setdefault(new_section, []).append(body)
        elif cell_type == "markdown" and role == "interpretation" and new_section:
            work = _interpretation_work(old_section, str(cell.get("source") or ""))
            if work:
                migrated_interpretations[new_section] = work
        elif cell_type == "markdown" and role == "final-conclusion":
            migrated_final = _final_conclusion_work(str(cell.get("source") or ""))

    has_work = bool(migrated_sql or migrated_interpretations or migrated_final or has_outputs)
    archive = _archive_notebook(plan, path) if has_work or not metadata.get("dcaManaged") else None

    new_payload = relationship_notebook_payload(plan)
    for cell in new_payload.get("cells") or []:
        cell_metadata = cell.get("metadata") or {}
        role = str(cell_metadata.get("dcaRole") or "")
        section = str(cell_metadata.get("dcaSection") or "")
        if role == "learner-sql" and section in migrated_sql:
            cell["source"] = "%%sql\n\n" + "\n\n".join(migrated_sql[section]).strip() + "\n"
        elif role == "interpretation" and section in migrated_interpretations:
            cell["source"] = migrated_interpretations[section].rstrip() + "\n"
        elif role == "final-conclusion" and migrated_final:
            cell["source"] = (
                str(cell.get("source") or "").rstrip()
                + "\n\n## Notes migrated from the previous notebook\n\n"
                + migrated_final.rstrip()
                + "\n"
            )

    path.write_text(json.dumps(new_payload, indent=2, ensure_ascii=False) + "\n", encoding="utf-8")
    if archive is not None:
        plan.warnings.append(
            "The relationship-validation notebook was upgraded to the clean v4 workflow. "
            f"The previous notebook was archived at {archive.relative_to(plan.project_dir).as_posix()}."
        )
    else:
        plan.warnings.append(
            "The untouched relationship-validation notebook was upgraded to the clean v4 workflow."
        )
    return True

def _write_notebook(plan: ProjectDataPlan, *, refresh: bool = False) -> None:
    if plan.notebook_path is None:
        raise ProjectDataError("Project data plan is missing its notebook path.")
    path = plan.notebook_path
    path.parent.mkdir(parents=True, exist_ok=True)
    if not path.exists():
        path.write_text(relationship_notebook_json(plan), encoding="utf-8")
        _ensure_notebook_kernel(path)
        return

    if _upgrade_notebook_to_current(path, plan):
        _ensure_notebook_kernel(path)
        return

    if refresh and _notebook_can_refresh(path):
        path.write_text(relationship_notebook_json(plan), encoding="utf-8")
    elif refresh:
        plan.warnings.append(
            "The existing relationship-validation notebook contains work or outputs, "
            "so it was preserved while the project data was refreshed."
        )

    # Kernel metadata is safe to repair even when learner work or outputs exist.
    _ensure_notebook_kernel(path)


def workspace_payload(plan: ProjectDataPlan) -> dict[str, Any]:
    return {
        "dcaManaged": True,
        "folders": [{"name": plan.project_name, "path": "."}],
        "settings": {
            "files.associations": {"*.sql": "sql", "*.ipynb": "jupyter-notebook"},
            "python.defaultInterpreterPath": "${workspaceFolder}/../../.venv/Scripts/python.exe",
            # The notebook uses its own in-memory DuckDB session. Do not auto-attach
            # project.duckdb here because the VS Code extension can hold a Windows file
            # lock that prevents the Jupyter kernel from opening the same database.
            "duckdb.resultsLocation": "beside",
        },
        "extensions": {
            "recommendations": ["ms-python.python", "ms-toolsai.jupyter"]
        },
    }


def _write_workspace(plan: ProjectDataPlan) -> None:
    if plan.workspace_path is None:
        return
    path = plan.workspace_path
    if path.exists():
        try:
            existing = json.loads(path.read_text(encoding="utf-8"))
        except (OSError, UnicodeError, json.JSONDecodeError):
            existing = None
        if isinstance(existing, dict) and not existing.get("dcaManaged"):
            path = path.with_name(path.stem + "-dca.code-workspace")
            plan.workspace_path = path
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(workspace_payload(plan), indent=2) + "\n", encoding="utf-8")


def _write_once(path: Path | None, content: str) -> None:
    if path is None or path.exists():
        return
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content, encoding="utf-8")


def _same_text(left: str, right: str) -> bool:
    return left.replace("\r\n", "\n").strip() == right.replace("\r\n", "\n").strip()


def _archive_path(plan: ProjectDataPlan, source: Path) -> Path:
    stamp = datetime.now().strftime("%Y%m%d-%H%M%S")
    try:
        relative = source.relative_to(plan.project_dir)
    except ValueError:
        relative = Path(source.name)
    return plan.project_dir / "archive" / "portfolio-workspace-migration" / stamp / relative


def _record_migration(plan: ProjectDataPlan, actions: list[str]) -> None:
    if not actions or plan.migration_report_path is None:
        return
    path = plan.migration_report_path
    path.parent.mkdir(parents=True, exist_ok=True)
    heading = "# Portfolio SQL Workspace Migration\n\n"
    if not path.exists():
        path.write_text(heading, encoding="utf-8")
    with path.open("a", encoding="utf-8") as handle:
        handle.write(f"## {datetime.now().isoformat(timespec='seconds')}\n\n")
        for action in actions:
            handle.write(f"- {action}\n")
        handle.write("\n")


def _remove_or_archive_legacy(plan: ProjectDataPlan) -> list[str]:
    actions: list[str] = []
    expected = {
        LEGACY_SETUP_RELATIVES[0]: setup_sql(plan),
        LEGACY_SETUP_RELATIVES[1]: setup_sql(plan),
        LEGACY_VALIDATION_RELATIVES[0]: _legacy_primary_key_sql(plan),
        LEGACY_VALIDATION_RELATIVES[1]: _legacy_orphan_sql(plan),
        LEGACY_VALIDATION_RELATIVES[2]: _legacy_cardinality_sql(plan),
        LEGACY_VALIDATION_RELATIVES[3]: _legacy_consistency_sql(plan),
        LEGACY_STARTER_SQL_RELATIVE: relationship_starter_sql(plan),
        LEGACY_FINDINGS_RELATIVE: relationship_findings_markdown(plan),
    }
    for relative, generated in expected.items():
        path = plan.project_dir / relative
        if not path.is_file():
            continue
        try:
            current = path.read_text(encoding="utf-8")
        except (OSError, UnicodeError):
            continue
        if _same_text(current, generated):
            path.unlink()
            actions.append(f"Deleted outdated generated file `{relative.as_posix()}`.")
        elif any(
            marker in current
            for marker in (MANAGED_HEADER, STARTER_HEADER, FINDINGS_HEADER)
        ):
            archive = _archive_path(plan, path)
            archive.parent.mkdir(parents=True, exist_ok=True)
            shutil.move(str(path), str(archive))
            actions.append(
                f"Archived edited legacy file `{relative.as_posix()}` to "
                f"`{archive.relative_to(plan.project_dir).as_posix()}`."
            )
    setup_dir = plan.project_dir / "sql" / "setup"
    try:
        if setup_dir.is_dir() and not any(setup_dir.iterdir()):
            setup_dir.rmdir()
            actions.append("Removed the empty legacy `sql/setup/` folder.")
    except OSError:
        pass
    return actions


def _migrate_config(plan: ProjectDataPlan) -> list[str]:
    actions: list[str] = []
    if plan.config_path is None:
        return actions
    plan.config_path.parent.mkdir(parents=True, exist_ok=True)
    for relative in LEGACY_CONFIG_RELATIVES:
        old = plan.project_dir / relative
        if not old.is_file():
            continue
        if not plan.config_path.exists():
            shutil.move(str(old), str(plan.config_path))
            actions.append(
                f"Moved source configuration to `{CONFIG_RELATIVE.as_posix()}`."
            )
        else:
            try:
                same = _same_text(
                    old.read_text(encoding="utf-8"),
                    plan.config_path.read_text(encoding="utf-8"),
                )
            except (OSError, UnicodeError):
                same = False
            if same:
                old.unlink()
                actions.append(f"Deleted duplicate legacy configuration `{relative.as_posix()}`.")
            else:
                archive = _archive_path(plan, old)
                archive.parent.mkdir(parents=True, exist_ok=True)
                shutil.move(str(old), str(archive))
                actions.append(
                    f"Archived legacy configuration to `{archive.relative_to(plan.project_dir).as_posix()}`."
                )
    return actions


def write_generated_files(plan: ProjectDataPlan, *, refresh: bool = False) -> None:
    if plan.config_path is None:
        raise ProjectDataError("Project data plan is missing its source configuration path.")
    actions = _migrate_config(plan)
    plan.config_path.parent.mkdir(parents=True, exist_ok=True)
    if refresh or not plan.config_path.exists():
        plan.config_path.write_text(
            yaml.safe_dump(_config_payload(plan), sort_keys=False, allow_unicode=True),
            encoding="utf-8",
        )
    _write_workspace(plan)
    _write_notebook(plan, refresh=refresh)
    actions.extend(_remove_or_archive_legacy(plan))
    _record_migration(plan, actions)

def build_database(plan: ProjectDataPlan) -> bool:
    if plan.database_path is None:
        return False
    if not plan.sources:
        plan.warnings.append("The project database was not built because no sources were found.")
        return False
    try:
        import duckdb
    except Exception:
        plan.warnings.append("DuckDB is not installed, so the project database was not built.")
        return False
    plan.database_path.parent.mkdir(parents=True, exist_ok=True)
    try:
        connection = duckdb.connect(str(plan.database_path))
        try:
            connection.execute(f"CREATE SCHEMA IF NOT EXISTS {_qid(plan.schema)}")
            for source in plan.sources:
                source_path = _safe_relative(plan.project_dir, source.path)
                reader = _reader_sql(source_path, source.format, absolute=True)
                connection.execute(
                    f"CREATE OR REPLACE VIEW {_qid(plan.schema)}.{_qid(source.view)} "
                    f"AS SELECT * FROM {reader}"
                )
        finally:
            connection.close()
    except Exception as exc:
        plan.warnings.append(f"Could not build the project DuckDB workspace: {exc}")
        return False
    plan.database_ready = plan.database_path.is_file()
    return plan.database_ready


def _refresh_discovered_sources(plan: ProjectDataPlan) -> None:
    discovered = discover_sources(plan.project_dir)
    existing_by_path = {source.path.casefold(): source for source in plan.sources}
    merged: list[ProjectSource] = []
    used: set[str] = set()
    for item in discovered:
        previous = existing_by_path.get(item.path.casefold())
        if previous is None:
            view = _unique_view(item.view, used)
            merged.append(
                ProjectSource(
                    name=item.name,
                    path=item.path,
                    format=item.format,
                    view=view,
                    columns=item.columns,
                    primary_key=item.primary_key,
                )
            )
            continue
        view = _unique_view(previous.view, used)
        merged.append(
            ProjectSource(
                name=previous.name,
                path=item.path,
                format=item.format,
                view=view,
                columns=item.columns,
                primary_key=previous.primary_key or item.primary_key,
            )
        )
    plan.sources = merged
    valid_views = {source.view for source in merged}
    preserved = [
        relationship
        for relationship in plan.relationships
        if relationship.parent in valid_views and relationship.child in valid_views
    ]
    seen = {
        (item.parent, item.parent_key, item.child, item.child_key)
        for item in preserved
    }
    for item in infer_relationships(merged):
        key = (item.parent, item.parent_key, item.child, item.child_key)
        if key not in seen:
            preserved.append(item)
            seen.add(key)
    plan.relationships = sorted(
        preserved,
        key=lambda item: (item.parent, item.child, item.child_key),
    )


def prepare_project_data_workspace(
    root: Path,
    project_id: int,
    *,
    refresh: bool = False,
    build: bool = True,
) -> ProjectDataPlan:
    root = Path(root).resolve()
    project_dir = _project_dir(root, int(project_id))
    if not project_dir.is_dir():
        raise ProjectDataError(f"Project folder does not exist: {project_dir}")
    plan = _load_config(root, int(project_id), project_dir)
    if plan is None:
        plan = _new_plan(root, int(project_id), project_dir)
    elif refresh:
        _refresh_discovered_sources(plan)
    if build:
        build_database(plan)
    elif plan.database_path is not None:
        plan.database_ready = plan.database_path.is_file()
    write_generated_files(plan, refresh=refresh)
    return plan

def _repo_relative(root: Path, path: Path | None) -> str:
    if path is None:
        return ""
    try:
        return path.resolve().relative_to(Path(root).resolve()).as_posix()
    except ValueError:
        return str(path)


