def _carried_forward_notes(content: str) -> str:
    notes: list[str] = []
    for line in str(content or "").splitlines():
        stripped = line.strip()
        if re.match(r"^- \[[xX]\]", stripped):
            notes.append(stripped)
        elif stripped.startswith("|") and "Yes / No" not in stripped:
            cells = [cell.strip() for cell in stripped.strip("|").split("|")]
            if cells and not all(not cell or set(cell) <= {"-", ":"} for cell in cells):
                if any(cell and cell not in {"Relationship", "Parent duplicates", "Orphan children", "Child rows", "Joined rows", "Safe to use?", "Resolution"} for cell in cells):
                    if any(cell for cell in cells if cell not in {"", "Yes / No"}):
                        notes.append(stripped)
    return "\n".join(dict.fromkeys(notes))


def upgrade_relationship_guide(root: Path, plan: ProjectDataPlan, content: str) -> str:
    text = str(content or "")
    fresh = relationship_guide_markdown(root, plan, "Validate relationships")
    if not text.strip():
        return fresh
    if MANAGED_GUIDE_START in text and (
        "Step 1 — Review the generated source configuration" in text
        or "Run `sql/validation/01_primary_key_checks.sql`" in text
        or "Portable setup SQL" in text
    ):
        carried = _carried_forward_notes(text)
        if carried:
            fresh += (
                "\n\n## Notes carried forward from the previous guide\n\n"
                "Review these entries and move confirmed results into the new findings document.\n\n"
                + carried
                + "\n"
            )
        return fresh

    managed = guide_managed_block(root, plan)
    if MANAGED_GUIDE_START in text and MANAGED_GUIDE_END in text:
        pattern = re.compile(
            re.escape(MANAGED_GUIDE_START) + r".*?" + re.escape(MANAGED_GUIDE_END),
            flags=re.S,
        )
        return pattern.sub(managed, text, count=1)

    insertion = text.find("\n## ")
    if insertion >= 0:
        next_heading = text.find("\n## ", insertion + 1)
        position = next_heading if next_heading >= 0 else len(text)
        return text[:position] + "\n\n" + managed + text[position:]
    return text.rstrip() + "\n\n" + managed + "\n"


def _vscode_command() -> str | None:
    for command in ("code", "code.cmd"):
        found = shutil.which(command)
        if found:
            return found
    if os.name == "nt":
        candidates = []
        local = os.environ.get("LOCALAPPDATA")
        program_files = os.environ.get("ProgramFiles")
        program_files_x86 = os.environ.get("ProgramFiles(x86)")
        if local:
            candidates.extend(
                [
                    Path(local) / "Programs" / "Microsoft VS Code" / "bin" / "code.cmd",
                    Path(local) / "Programs" / "Microsoft VS Code Insiders" / "bin" / "code-insiders.cmd",
                ]
            )
        for base in (program_files, program_files_x86):
            if base:
                candidates.extend(
                    [
                        Path(base) / "Microsoft VS Code" / "bin" / "code.cmd",
                        Path(base) / "Microsoft VS Code Insiders" / "bin" / "code-insiders.cmd",
                    ]
                )
        for candidate in candidates:
            if candidate.is_file():
                return str(candidate)
    return None


def open_starter_in_vscode(plan: ProjectDataPlan) -> str:
    if not plan.database_ready or plan.database_path is None or not plan.database_path.is_file():
        raise ProjectDataError("The project database is not ready. Refresh the project data first.")
    if plan.starter_sql_path is None or not plan.starter_sql_path.is_file():
        raise ProjectDataError("The SQL starter file is missing. Refresh the project data first.")
    if plan.workspace_path is None or not plan.workspace_path.is_file():
        raise ProjectDataError("The VS Code workspace is missing. Refresh the project data first.")
    command = _vscode_command()
    if command:
        subprocess.Popen(
            [command, "-r", str(plan.workspace_path), "-g", str(plan.starter_sql_path)],
            cwd=str(plan.project_dir),
        )
        return "VS Code"
    if os.name == "nt":
        os.startfile(str(plan.workspace_path))
        os.startfile(str(plan.starter_sql_path))
        return "VS Code or the default editor"
    if sys.platform == "darwin":
        subprocess.Popen(["open", "-a", "Visual Studio Code", str(plan.workspace_path)])
        subprocess.Popen(["open", str(plan.starter_sql_path)])
        return "VS Code"
    raise ProjectDataError(
        "VS Code could not be found. Open the generated .code-workspace file manually, "
        f"then open {plan.starter_sql_path}."
    )


