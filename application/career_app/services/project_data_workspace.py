@dataclass(frozen=True)
class ProjectSource:
    name: str
    path: str
    format: str
    view: str
    columns: tuple[str, ...] = ()
    primary_key: str = ""


@dataclass(frozen=True)
class ProjectRelationship:
    parent: str
    parent_key: str
    child: str
    child_key: str
    cardinality: str = "one-to-many"
    inferred: bool = True


@dataclass
class ProjectDataPlan:
    project_id: int
    project_name: str
    project_dir: Path
    schema: str = "raw"
    database_path: Path | None = None
    config_path: Path | None = None
    notebook_path: Path | None = None
    starter_sql_path: Path | None = None
    findings_path: Path | None = None
    workspace_path: Path | None = None
    migration_report_path: Path | None = None
    sources: list[ProjectSource] = field(default_factory=list)
    relationships: list[ProjectRelationship] = field(default_factory=list)
    database_ready: bool = False
    warnings: list[str] = field(default_factory=list)

    @property
    def source_count(self) -> int:
        return len(self.sources)

    @property
    def relationship_count(self) -> int:
        return len(self.relationships)


class ProjectDataError(ValueError):
    pass


def _project_dir(root: Path, project_id: int) -> Path:
    try:
        directory = PROJECT_DIRS[int(project_id)]
    except KeyError as exc:
        raise ProjectDataError(f"Unknown portfolio project: {project_id}") from exc
    return (Path(root) / "projects" / directory).resolve()


def _safe_relative(project_dir: Path, value: str | Path) -> Path:
    candidate = Path(value)
    if candidate.is_absolute():
        resolved = candidate.resolve()
    else:
        resolved = (project_dir / candidate).resolve()
    try:
        resolved.relative_to(project_dir)
    except ValueError as exc:
        raise ProjectDataError("Project data path leaves the project folder.") from exc
    return resolved


def _slug(value: str, fallback: str = "table") -> str:
    value = re.sub(r"[^a-zA-Z0-9_]+", "_", str(value or "")).strip("_").lower()
    value = re.sub(r"_+", "_", value)
    if value.startswith("raw_"):
        value = value[4:]
    if not value:
        value = fallback
    if value[0].isdigit():
        value = f"t_{value}"
    return value


def _singular(value: str) -> str:
    text = str(value)
    if text.endswith("ies") and len(text) > 3:
        return text[:-3] + "y"
    if text.endswith("ses") and len(text) > 3:
        return text[:-2]
    if text.endswith("s") and not text.endswith("ss") and len(text) > 1:
        return text[:-1]
    return text


def _sql_string(value: str | Path) -> str:
    return "'" + str(value).replace("\\", "/").replace("'", "''") + "'"


def _qid(value: str) -> str:
    return '"' + str(value).replace('"', '""') + '"'


def _format_from_suffix(path: Path) -> str:
    suffix = path.suffix.casefold()
    if suffix == ".csv":
        return "csv"
    if suffix == ".parquet":
        return "parquet"
    if suffix in {".jsonl", ".ndjson"}:
        return "json_lines"
    if suffix == ".json":
        return "json"
    raise ProjectDataError(f"Unsupported project data file: {path.name}")


def _csv_columns(path: Path) -> tuple[str, ...]:
    try:
        with path.open("r", encoding="utf-8-sig", newline="") as handle:
            sample = handle.read(8192)
            handle.seek(0)
            try:
                dialect = csv.Sniffer().sniff(sample, delimiters=",;\t|")
            except csv.Error:
                dialect = csv.excel
            row = next(csv.reader(handle, dialect), [])
    except (OSError, UnicodeError):
        return ()
    return tuple(str(value).strip() for value in row if str(value).strip())


def _json_columns(path: Path) -> tuple[str, ...]:
    try:
        with path.open("r", encoding="utf-8") as handle:
            if path.suffix.casefold() in {".jsonl", ".ndjson"}:
                first = next((line for line in handle if line.strip()), "")
                value = json.loads(first) if first else {}
            else:
                value = json.load(handle)
        if isinstance(value, list):
            value = next((item for item in value if isinstance(item, dict)), {})
        if isinstance(value, dict):
            return tuple(str(key) for key in value)
    except (OSError, UnicodeError, json.JSONDecodeError):
        return ()
    return ()


def _duckdb_columns(path: Path, source_format: str) -> tuple[str, ...]:
    try:
        import duckdb
    except Exception:
        return ()
    reader = _reader_sql(path, source_format, absolute=True)
    try:
        connection = duckdb.connect(":memory:")
        try:
            rows = connection.execute(f"DESCRIBE SELECT * FROM {reader}").fetchall()
            return tuple(str(row[0]) for row in rows)
        finally:
            connection.close()
    except Exception:
        return ()


def _source_columns(path: Path, source_format: str) -> tuple[str, ...]:
    if source_format == "csv":
        columns = _csv_columns(path)
    elif source_format in {"json", "json_lines"}:
        columns = _json_columns(path)
    else:
        columns = ()
    return columns or _duckdb_columns(path, source_format)


def _infer_primary_key(view: str, columns: Iterable[str]) -> str:
    available = {str(column).casefold(): str(column) for column in columns}
    singular = _singular(view)
    candidates = (
        f"{singular}_id",
        f"{view}_id",
        "id",
    )
    for candidate in candidates:
        if candidate.casefold() in available:
            return available[candidate.casefold()]
    id_columns = [original for key, original in available.items() if key.endswith("_id")]
    return id_columns[0] if len(id_columns) == 1 else ""


def _unique_view(base: str, used: set[str]) -> str:
    candidate = base
    index = 2
    while candidate.casefold() in used:
        candidate = f"{base}_{index}"
        index += 1
    used.add(candidate.casefold())
    return candidate


def discover_sources(project_dir: Path) -> list[ProjectSource]:
    raw_dir = project_dir / "data" / "raw"
    if not raw_dir.is_dir():
        return []
    files = sorted(
        path
        for path in raw_dir.rglob("*")
        if path.is_file()
        and path.suffix.casefold() in SUPPORTED_SUFFIXES
        and not any(part.startswith(".") for part in path.relative_to(raw_dir).parts)
    )
    used: set[str] = set()
    sources: list[ProjectSource] = []
    for path in files:
        view = _unique_view(_slug(path.stem), used)
        source_format = _format_from_suffix(path)
        columns = _source_columns(path, source_format)
        sources.append(
            ProjectSource(
                name=view,
                path=path.relative_to(project_dir).as_posix(),
                format=source_format,
                view=view,
                columns=columns,
                primary_key=_infer_primary_key(view, columns),
            )
        )
    return sources


def infer_relationships(sources: Iterable[ProjectSource]) -> list[ProjectRelationship]:
    source_list = list(sources)
    relationships: list[ProjectRelationship] = []
    seen: set[tuple[str, str, str, str]] = set()
    for parent in source_list:
        if not parent.primary_key:
            continue
        parent_key = parent.primary_key.casefold()
        for child in source_list:
            if child.view == parent.view:
                continue
            match = ""
            for column in child.columns:
                lowered = str(column).casefold()
                if lowered == parent_key or lowered.endswith("_" + parent_key):
                    match = str(column)
                    break
            if not match:
                continue
            key = (parent.view, parent.primary_key, child.view, match)
            if key in seen:
                continue
            seen.add(key)
            relationships.append(
                ProjectRelationship(
                    parent=parent.view,
                    parent_key=parent.primary_key,
                    child=child.view,
                    child_key=match,
                )
            )
    return sorted(
        relationships,
        key=lambda item: (item.parent, item.child, item.child_key),
    )


def _source_dict(source: ProjectSource) -> dict[str, Any]:
    return {
        "name": source.name,
        "path": source.path,
        "format": source.format,
        "view": source.view,
        "primary_key": source.primary_key or None,
        "columns": list(source.columns),
    }


def _relationship_dict(item: ProjectRelationship) -> dict[str, Any]:
    return {
        "parent": item.parent,
        "parent_key": item.parent_key,
        "child": item.child,
        "child_key": item.child_key,
        "cardinality": item.cardinality,
        "inferred": bool(item.inferred),
    }


def _config_payload(plan: ProjectDataPlan) -> dict[str, Any]:
    return {
        "version": CONFIG_VERSION,
        "generated_by": "Data Career Accelerator",
        "generated_at": datetime.now().isoformat(timespec="seconds"),
        "schema": plan.schema,
        "database": DATABASE_RELATIVE.as_posix(),
        "sources": [_source_dict(source) for source in plan.sources],
        "relationships": [_relationship_dict(item) for item in plan.relationships],
    }


def _plan_from_payload(
    root: Path,
    project_id: int,
    project_dir: Path,
    payload: dict[str, Any],
) -> ProjectDataPlan:
    schema = _slug(str(payload.get("schema") or "raw"), "raw")
    sources: list[ProjectSource] = []
    used: set[str] = set()
    for index, raw in enumerate(payload.get("sources") or [], start=1):
        if not isinstance(raw, dict):
            continue
        path_value = str(raw.get("path") or "").strip()
        if not path_value:
            continue
        source_path = _safe_relative(project_dir, path_value)
        if not source_path.is_file():
            continue
        source_format = str(raw.get("format") or _format_from_suffix(source_path)).strip()
        view = _unique_view(
            _slug(str(raw.get("view") or raw.get("name") or source_path.stem), f"table_{index}"),
            used,
        )
        columns = tuple(str(value) for value in (raw.get("columns") or ()))
        if not columns:
            columns = _source_columns(source_path, source_format)
        primary_key = str(raw.get("primary_key") or "").strip()
        if not primary_key:
            primary_key = _infer_primary_key(view, columns)
        sources.append(
            ProjectSource(
                name=str(raw.get("name") or view),
                path=source_path.relative_to(project_dir).as_posix(),
                format=source_format,
                view=view,
                columns=columns,
                primary_key=primary_key,
            )
        )
    relationships: list[ProjectRelationship] = []
    for raw in payload.get("relationships") or []:
        if not isinstance(raw, dict):
            continue
        required = ("parent", "parent_key", "child", "child_key")
        if not all(str(raw.get(key) or "").strip() for key in required):
            continue
        relationships.append(
            ProjectRelationship(
                parent=_slug(str(raw["parent"])),
                parent_key=str(raw["parent_key"]),
                child=_slug(str(raw["child"])),
                child_key=str(raw["child_key"]),
                cardinality=str(raw.get("cardinality") or "one-to-many"),
                inferred=bool(raw.get("inferred", False)),
            )
        )
    if not relationships:
        relationships = infer_relationships(sources)
    database_value = str(payload.get("database") or DATABASE_RELATIVE.as_posix())
    return ProjectDataPlan(
        project_id=int(project_id),
        project_name=PROJECT_NAMES.get(int(project_id), f"Project {int(project_id)}"),
        project_dir=project_dir,
        schema=schema,
        database_path=_safe_relative(project_dir, database_value),
        config_path=project_dir / CONFIG_RELATIVE,
        notebook_path=project_dir / NOTEBOOK_RELATIVE,
        starter_sql_path=project_dir / LEGACY_STARTER_SQL_RELATIVE,
        findings_path=project_dir / LEGACY_FINDINGS_RELATIVE,
        workspace_path=project_dir / f"{project_dir.name}.code-workspace",
        migration_report_path=project_dir / "documentation" / "portfolio_workspace_migration.md",
        sources=sources,
        relationships=relationships,
    )


def _load_config(root: Path, project_id: int, project_dir: Path) -> ProjectDataPlan | None:
    candidates = [project_dir / CONFIG_RELATIVE]
    candidates.extend(project_dir / relative for relative in LEGACY_CONFIG_RELATIVES)
    path = next((candidate for candidate in candidates if candidate.is_file()), None)
    if path is None:
        return None
    try:
        payload = yaml.safe_load(path.read_text(encoding="utf-8")) or {}
    except (OSError, UnicodeError, yaml.YAMLError) as exc:
        raise ProjectDataError(f"Could not read {path.relative_to(project_dir).as_posix()}: {exc}") from exc
    if not isinstance(payload, dict):
        raise ProjectDataError("Project data configuration must be a YAML mapping.")
    plan = _plan_from_payload(root, project_id, project_dir, payload)
    if path != plan.config_path:
        plan.warnings.append(
            f"The source configuration will be moved from {path.relative_to(project_dir).as_posix()} "
            f"to {CONFIG_RELATIVE.as_posix()}."
        )
    return plan

