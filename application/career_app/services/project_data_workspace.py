def _write_workspace(plan: ProjectDataPlan) -> None:
    if plan.workspace_path is None:
        return
    path = plan.workspace_path
    if path.exists():
        try:
            existing = json.loads(path.read_text(encoding="utf-8"))
        except (OSError, UnicodeError, json.JSONDecodeError):
            existing = None
        if isinstance(existing, dict) and not existing.get("dcaManaged"):
            path = path.with_name(path.stem + "-dca.code-workspace")
            plan.workspace_path = path
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(workspace_payload(plan), indent=2) + "\n", encoding="utf-8")


def _write_once(path: Path | None, content: str) -> None:
    if path is None or path.exists():
        return
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content, encoding="utf-8")


def _same_text(left: str, right: str) -> bool:
    return left.replace("\r\n", "\n").strip() == right.replace("\r\n", "\n").strip()


def _archive_path(plan: ProjectDataPlan, source: Path) -> Path:
    stamp = datetime.now().strftime("%Y%m%d-%H%M%S")
    try:
        relative = source.relative_to(plan.project_dir)
    except ValueError:
        relative = Path(source.name)
    return plan.project_dir / "archive" / "portfolio-workspace-migration" / stamp / relative


def _record_migration(plan: ProjectDataPlan, actions: list[str]) -> None:
    if not actions or plan.migration_report_path is None:
        return
    path = plan.migration_report_path
    path.parent.mkdir(parents=True, exist_ok=True)
    heading = "# Portfolio SQL Workspace Migration\n\n"
    if not path.exists():
        path.write_text(heading, encoding="utf-8")
    with path.open("a", encoding="utf-8") as handle:
        handle.write(f"## {datetime.now().isoformat(timespec='seconds')}\n\n")
        for action in actions:
            handle.write(f"- {action}\n")
        handle.write("\n")


def _remove_or_archive_legacy(plan: ProjectDataPlan) -> list[str]:
    actions: list[str] = []
    expected = {
        LEGACY_SETUP_RELATIVES[0]: setup_sql(plan),
        LEGACY_SETUP_RELATIVES[1]: setup_sql(plan),
        LEGACY_VALIDATION_RELATIVES[0]: _legacy_primary_key_sql(plan),
        LEGACY_VALIDATION_RELATIVES[1]: _legacy_orphan_sql(plan),
        LEGACY_VALIDATION_RELATIVES[2]: _legacy_cardinality_sql(plan),
        LEGACY_VALIDATION_RELATIVES[3]: _legacy_consistency_sql(plan),
    }
    for relative, generated in expected.items():
        path = plan.project_dir / relative
        if not path.is_file():
            continue
        try:
            current = path.read_text(encoding="utf-8")
        except (OSError, UnicodeError):
            continue
        if _same_text(current, generated):
            path.unlink()
            actions.append(f"Deleted outdated generated file `{relative.as_posix()}`.")
        elif MANAGED_HEADER in current:
            archive = _archive_path(plan, path)
            archive.parent.mkdir(parents=True, exist_ok=True)
            shutil.move(str(path), str(archive))
            actions.append(
                f"Archived edited legacy file `{relative.as_posix()}` to "
                f"`{archive.relative_to(plan.project_dir).as_posix()}`."
            )
    setup_dir = plan.project_dir / "sql" / "setup"
    try:
        if setup_dir.is_dir() and not any(setup_dir.iterdir()):
            setup_dir.rmdir()
            actions.append("Removed the empty legacy `sql/setup/` folder.")
    except OSError:
        pass
    return actions


def _migrate_config(plan: ProjectDataPlan) -> list[str]:
    actions: list[str] = []
    if plan.config_path is None:
        return actions
    plan.config_path.parent.mkdir(parents=True, exist_ok=True)
    for relative in LEGACY_CONFIG_RELATIVES:
        old = plan.project_dir / relative
        if not old.is_file():
            continue
        if not plan.config_path.exists():
            shutil.move(str(old), str(plan.config_path))
            actions.append(
                f"Moved source configuration to `{CONFIG_RELATIVE.as_posix()}`."
            )
        else:
            try:
                same = _same_text(
                    old.read_text(encoding="utf-8"),
                    plan.config_path.read_text(encoding="utf-8"),
                )
            except (OSError, UnicodeError):
                same = False
            if same:
                old.unlink()
                actions.append(f"Deleted duplicate legacy configuration `{relative.as_posix()}`.")
            else:
                archive = _archive_path(plan, old)
                archive.parent.mkdir(parents=True, exist_ok=True)
                shutil.move(str(old), str(archive))
                actions.append(
                    f"Archived legacy configuration to `{archive.relative_to(plan.project_dir).as_posix()}`."
                )
    return actions


def write_generated_files(plan: ProjectDataPlan, *, refresh: bool = False) -> None:
    if plan.config_path is None:
        raise ProjectDataError("Project data plan is missing its source configuration path.")
    actions = _migrate_config(plan)
    plan.config_path.parent.mkdir(parents=True, exist_ok=True)
    if refresh or not plan.config_path.exists():
        plan.config_path.write_text(
            yaml.safe_dump(_config_payload(plan), sort_keys=False, allow_unicode=True),
            encoding="utf-8",
        )
    _write_workspace(plan)
    _write_once(plan.starter_sql_path, relationship_starter_sql(plan))
    _write_once(plan.findings_path, relationship_findings_markdown(plan))
    actions.extend(_remove_or_archive_legacy(plan))
    _record_migration(plan, actions)

def build_database(plan: ProjectDataPlan) -> bool:
    if plan.database_path is None:
        return False
    if not plan.sources:
        plan.warnings.append("The project database was not built because no sources were found.")
        return False
    try:
        import duckdb
    except Exception:
        plan.warnings.append("DuckDB is not installed, so the project database was not built.")
        return False
    plan.database_path.parent.mkdir(parents=True, exist_ok=True)
    try:
        connection = duckdb.connect(str(plan.database_path))
        try:
            connection.execute(f"CREATE SCHEMA IF NOT EXISTS {_qid(plan.schema)}")
            for source in plan.sources:
                source_path = _safe_relative(plan.project_dir, source.path)
                reader = _reader_sql(source_path, source.format, absolute=True)
                connection.execute(
                    f"CREATE OR REPLACE VIEW {_qid(plan.schema)}.{_qid(source.view)} "
                    f"AS SELECT * FROM {reader}"
                )
        finally:
            connection.close()
    except Exception as exc:
        plan.warnings.append(f"Could not build the project DuckDB workspace: {exc}")
        return False
    plan.database_ready = plan.database_path.is_file()
    return plan.database_ready


def _refresh_discovered_sources(plan: ProjectDataPlan) -> None:
    discovered = discover_sources(plan.project_dir)
    existing_by_path = {source.path.casefold(): source for source in plan.sources}
    merged: list[ProjectSource] = []
    used: set[str] = set()
    for item in discovered:
        previous = existing_by_path.get(item.path.casefold())
        if previous is None:
            view = _unique_view(item.view, used)
            merged.append(
                ProjectSource(
                    name=item.name,
                    path=item.path,
                    format=item.format,
                    view=view,
                    columns=item.columns,
                    primary_key=item.primary_key,
                )
            )
            continue
        view = _unique_view(previous.view, used)
        merged.append(
            ProjectSource(
                name=previous.name,
                path=item.path,
                format=item.format,
                view=view,
                columns=item.columns,
                primary_key=previous.primary_key or item.primary_key,
            )
        )
    plan.sources = merged
    valid_views = {source.view for source in merged}
    preserved = [
        relationship
        for relationship in plan.relationships
        if relationship.parent in valid_views and relationship.child in valid_views
    ]
    seen = {
        (item.parent, item.parent_key, item.child, item.child_key)
        for item in preserved
    }
    for item in infer_relationships(merged):
        key = (item.parent, item.parent_key, item.child, item.child_key)
        if key not in seen:
            preserved.append(item)
            seen.add(key)
    plan.relationships = sorted(
        preserved,
        key=lambda item: (item.parent, item.child, item.child_key),
    )


def prepare_project_data_workspace(
    root: Path,
    project_id: int,
    *,
    refresh: bool = False,
    build: bool = True,
) -> ProjectDataPlan:
    root = Path(root).resolve()
    project_dir = _project_dir(root, int(project_id))
    if not project_dir.is_dir():
        raise ProjectDataError(f"Project folder does not exist: {project_dir}")
    plan = _load_config(root, int(project_id), project_dir)
    if plan is None:
        plan = _new_plan(root, int(project_id), project_dir)
    elif refresh:
        _refresh_discovered_sources(plan)
    if build:
        build_database(plan)
    elif plan.database_path is not None:
        plan.database_ready = plan.database_path.is_file()
    write_generated_files(plan, refresh=refresh)
    return plan

def _repo_relative(root: Path, path: Path | None) -> str:
    if path is None:
        return ""
    try:
        return path.resolve().relative_to(Path(root).resolve()).as_posix()
    except ValueError:
        return str(path)


