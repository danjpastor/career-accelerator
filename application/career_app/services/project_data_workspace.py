"""Project-neutral data-source discovery and DuckDB workspace generation.

Portfolio projects describe their raw data through a small YAML manifest.  When
no manifest exists, the service discovers supported files under ``data/raw``,
infers sensible table names and relationship candidates, and creates a reusable
DuckDB setup script plus validation starters.  The generated configuration is
project-local so the same engine works for any learner project.
"""

