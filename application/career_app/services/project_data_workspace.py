def _reader_sql(path: Path, source_format: str, *, absolute: bool = False) -> str:
    value = path.resolve() if absolute else path
    quoted = _sql_string(value)
    if source_format == "csv":
        return f"read_csv_auto({quoted}, header = true)"
    if source_format == "parquet":
        return f"read_parquet({quoted})"
    if source_format == "json_lines":
        return f"read_json_auto({quoted}, format = 'newline_delimited')"
    if source_format == "json":
        return f"read_json_auto({quoted})"
    raise ProjectDataError(f"Unsupported source format: {source_format}")


def setup_sql(plan: ProjectDataPlan, *, absolute_paths: bool = False) -> str:
    """Return the legacy registration script for migration comparisons only."""
    lines = [
        f"-- {MANAGED_HEADER}",
        "-- Re-run this script whenever the raw source files change.",
        f"CREATE SCHEMA IF NOT EXISTS {_qid(plan.schema)};",
        "",
    ]
    for source in plan.sources:
        source_path = _safe_relative(plan.project_dir, source.path)
        path_value = (
            source_path
            if absolute_paths
            else Path("projects") / plan.project_dir.name / source.path
        )
        reader = _reader_sql(path_value, source.format, absolute=absolute_paths)
        lines.extend(
            [
                f"CREATE OR REPLACE VIEW {_qid(plan.schema)}.{_qid(source.view)} AS",
                f"SELECT * FROM {reader};",
                "",
            ]
        )
    if not plan.sources:
        lines.append("-- Add supported source files under data/raw, then refresh this setup.")
    return "\n".join(lines).rstrip() + "\n"


def _legacy_primary_key_sql(plan: ProjectDataPlan) -> str:
    lines = [
        f"-- {MANAGED_HEADER}",
        "-- Each query should return zero rows when the key is unique.",
        "",
    ]
    for source in plan.sources:
        if not source.primary_key:
            continue
        table = f"{_qid(plan.schema)}.{_qid(source.view)}"
        key = _qid(source.primary_key)
        lines.extend(
            [
                f"-- {plan.schema}.{source.view}.{source.primary_key}",
                f"SELECT {key}, COUNT(*) AS row_count",
                f"FROM {table}",
                f"GROUP BY {key}",
                "HAVING COUNT(*) > 1;",
                "",
            ]
        )
    if len(lines) == 3:
        lines.append("-- No primary keys were inferred. Add them to data/project_sources.yaml.")
    return "\n".join(lines).rstrip() + "\n"


def _legacy_orphan_sql(plan: ProjectDataPlan) -> str:
    lines = [
        f"-- {MANAGED_HEADER}",
        "-- Each query should return zero rows when every child key has a parent.",
        "",
    ]
    for item in plan.relationships:
        parent = f"{_qid(plan.schema)}.{_qid(item.parent)}"
        child = f"{_qid(plan.schema)}.{_qid(item.child)}"
        parent_key = _qid(item.parent_key)
        child_key = _qid(item.child_key)
        lines.extend(
            [
                f"-- {item.parent} -> {item.child}",
                f"SELECT c.{child_key} AS missing_parent_key, COUNT(*) AS orphan_rows",
                f"FROM {child} AS c",
                f"LEFT JOIN {parent} AS p",
                f"    ON c.{child_key} = p.{parent_key}",
                f"WHERE c.{child_key} IS NOT NULL",
                f"  AND p.{parent_key} IS NULL",
                f"GROUP BY c.{child_key};",
                "",
            ]
        )
    if len(lines) == 3:
        lines.append("-- No relationships were inferred. Add them to data/project_sources.yaml.")
    return "\n".join(lines).rstrip() + "\n"


def _legacy_cardinality_sql(plan: ProjectDataPlan) -> str:
    lines = [
        f"-- {MANAGED_HEADER}",
        "-- For a valid many-to-one lookup, joined_rows should equal child_rows.",
        "",
    ]
    for item in plan.relationships:
        parent = f"{_qid(plan.schema)}.{_qid(item.parent)}"
        child = f"{_qid(plan.schema)}.{_qid(item.child)}"
        parent_key = _qid(item.parent_key)
        child_key = _qid(item.child_key)
        lines.extend(
            [
                f"-- {item.parent} -> {item.child}",
                "SELECT",
                f"    (SELECT COUNT(*) FROM {child}) AS child_rows,",
                "    COUNT(*) AS joined_rows",
                f"FROM {child} AS c",
                f"LEFT JOIN {parent} AS p",
                f"    ON c.{child_key} = p.{parent_key};",
                "",
            ]
        )
    if len(lines) == 3:
        lines.append("-- No relationships were inferred. Add them to data/project_sources.yaml.")
    return "\n".join(lines).rstrip() + "\n"


def _legacy_consistency_sql(plan: ProjectDataPlan) -> str:
    return (
        f"-- {MANAGED_HEADER}\n"
        "-- Add project-specific checks for duplicated relationship fields.\n"
        "-- Example: compare a child project_id with the project_id on its referenced parent.\n\n"
        "-- Record any cross-table consistency checks below.\n"
    )


def _table_schema_rows(plan: ProjectDataPlan, source: ProjectSource) -> list[tuple[str, str]]:
    if plan.database_path is not None and plan.database_path.is_file():
        try:
            import duckdb

            connection = duckdb.connect(str(plan.database_path), read_only=True)
            try:
                rows = connection.execute(
                    f"DESCRIBE SELECT * FROM {_qid(plan.schema)}.{_qid(source.view)}"
                ).fetchall()
                return [(str(row[0]), str(row[1])) for row in rows]
            finally:
                connection.close()
        except Exception:
            pass
    return [(column, "") for column in source.columns]


def relationship_starter_sql(plan: ProjectDataPlan) -> str:
    """Create a guided but intentionally incomplete learner SQL starter."""
    lines = [
        f"-- {STARTER_HEADER}",
        f"-- Project: {plan.project_name}",
        "--",
        "-- This project database is attached automatically when you open the generated",
        "-- VS Code workspace. Run a statement with Ctrl+Enter, or use the Run CodeLens.",
        "-- Write the real project queries in the TODO sections below.",
        "-- The examples use placeholder names and are not the answers to this task.",
        "",
        "-- ============================================================",
        "-- AVAILABLE TABLES",
        "-- ============================================================",
    ]
    for source in plan.sources:
        lines.append(f"-- {plan.schema}.{source.view}")
        schema_rows = _table_schema_rows(plan, source)
        if schema_rows:
            for column, data_type in schema_rows:
                suffix = f" ({data_type})" if data_type else ""
                key_note = " [primary key candidate]" if column == source.primary_key else ""
                lines.append(f"--   {column}{suffix}{key_note}")
        else:
            lines.append("--   Columns unavailable; refresh the project data workspace.")
        lines.append("")

    lines.extend(
        [
            "-- ============================================================",
            "-- RELATIONSHIPS TO VALIDATE",
            "-- ============================================================",
        ]
    )
    if plan.relationships:
        for item in plan.relationships:
            lines.append(
                f"-- {plan.schema}.{item.child}.{item.child_key} -> "
                f"{plan.schema}.{item.parent}.{item.parent_key}"
            )
    else:
        lines.append("-- Review config/project_sources.yaml and add the relationships for this project.")

    lines.extend(
        [
            "",
            "-- ============================================================",
            "-- 1. CHECK PRIMARY-KEY UNIQUENESS",
            "-- ============================================================",
            "-- Goal: find identifiers that appear more than once.",
            "-- Basic syntax example only:",
            "--",
            "-- SELECT example_id, COUNT(*) AS row_count",
            "-- FROM example_table",
            "-- GROUP BY example_id",
            "-- HAVING COUNT(*) > 1;",
            "--",
            "-- TODO: Write a duplicate-key check for each declared primary key.",
            "",
            "",
            "-- ============================================================",
            "-- 2. CHECK FOR MISSING PARENT RECORDS",
            "-- ============================================================",
            "-- Goal: find child rows whose foreign key has no matching parent.",
            "-- Helpful concepts: LEFT JOIN, matching key columns, and IS NULL.",
            "-- TODO: Write one orphan-key check for each required relationship above.",
            "",
            "",
            "-- ============================================================",
            "-- 3. CHECK JOIN CARDINALITY",
            "-- ============================================================",
            "-- Goal: compare the child row count before and after a many-to-one join.",
            "-- Ask: did the join unexpectedly multiply rows?",
            "-- TODO: Compare row counts for each relationship you plan to use.",
            "",
            "",
            "-- ============================================================",
            "-- 4. ADD PROJECT-SPECIFIC CONSISTENCY CHECKS",
            "-- ============================================================",
            "-- Goal: test business rules that cannot be inferred from column names alone.",
            "-- TODO: Add any cross-table checks required by this project's data model.",
            "",
        ]
    )
    return "\n".join(lines).rstrip() + "\n"


def relationship_findings_markdown(plan: ProjectDataPlan) -> str:
    relationship_rows = []
    for item in plan.relationships:
        relationship_rows.append(
            f"| `{plan.schema}.{item.child}` | `{item.child_key}` | "
            f"`{plan.schema}.{item.parent}` | `{item.parent_key}` | Not tested | |"
        )
    if not relationship_rows:
        relationship_rows.append("|  |  |  |  | Not tested | |")
    return (
        f"<!-- {FINDINGS_HEADER} -->\n"
        f"# {plan.project_name} — Relationship Validation\n\n"
        "Use this document to record what your SQL checks found. Replace the starter "
        "text with your own results and conclusions.\n\n"
        "## Relationships tested\n\n"
        "| Child table | Foreign key | Parent table | Parent key | Result | Notes |\n"
        "|---|---|---|---|---|---|\n"
        + "\n".join(relationship_rows)
        + "\n\n## Duplicate primary keys\n\n"
        "Describe any duplicate identifiers you found, or state that none were returned.\n\n"
        "## Orphaned foreign keys\n\n"
        "Describe child records that did not match a parent record, or state that none were returned.\n\n"
        "## Join cardinality\n\n"
        "Record whether each join preserved the expected child-row count.\n\n"
        "## Exceptions and decisions\n\n"
        "Document intentional exceptions, source-data problems, and any cleanup decisions.\n\n"
        "## Final conclusion\n\n"
        "State whether the tested relationships are safe to use for analysis and what must be fixed first.\n"
    )


def _markdown_cell(source: str, *, metadata: dict[str, Any] | None = None) -> dict[str, Any]:
    return {
        "cell_type": "markdown",
        "metadata": dict(metadata or {}),
        "source": str(source).rstrip() + "\n",
    }


def _code_cell(source: str, *, metadata: dict[str, Any] | None = None) -> dict[str, Any]:
    return {
        "cell_type": "code",
        "execution_count": None,
        "metadata": dict(metadata or {}),
        "outputs": [],
        "source": str(source).rstrip() + "\n",
    }


def _notebook_source_hash(cells: Iterable[dict[str, Any]]) -> str:
    normalized = []
    for cell in cells:
        normalized.append(
            {
                "cell_type": str(cell.get("cell_type") or ""),
                "source": cell.get("source") or "",
                "dcaRole": (cell.get("metadata") or {}).get("dcaRole"),
                "dcaSection": (cell.get("metadata") or {}).get("dcaSection"),
            }
        )
    payload = json.dumps(normalized, sort_keys=True, ensure_ascii=False).encode("utf-8")
    return hashlib.sha256(payload).hexdigest()


def _sql_magic_cell(section: str, prompt: str) -> dict[str, Any]:
    return _code_cell(
        f"%%sql\n\n-- {prompt}\n",
        metadata={
            "dcaRole": "learner-sql",
            "dcaSection": section,
            "vscode": {"languageId": "sql"},
        },
    )


def _interpretation_cell(section: str, prompt: str) -> dict[str, Any]:
    return _markdown_cell(
        prompt,
        metadata={"dcaRole": "interpretation", "dcaSection": section},
    )


def _setup_cell_source(plan: ProjectDataPlan) -> str:
    return f'''%load_ext sql
%config SqlMagic.feedback = False
%config SqlMagic.displaycon = False

from pathlib import Path
import duckdb
import yaml

cwd = Path.cwd().resolve()
project_root = next(
    (
        candidate
        for candidate in (cwd, *cwd.parents, cwd / "projects" / "{plan.project_dir.name}")
        if (candidate / "config" / "project_sources.yaml").is_file()
    ),
    None,
)
if project_root is None:
    raise FileNotFoundError(
        "Open this project's generated VS Code workspace, then run the setup cell again."
    )

config = yaml.safe_load(
    (project_root / "config" / "project_sources.yaml").read_text(encoding="utf-8")
) or {{}}
schema_name = str(config.get("schema") or "raw")

def qid(value):
    return '"' + str(value).replace('"', '""') + '"'

def qpath(value):
    return "'" + Path(value).resolve().as_posix().replace("'", "''") + "'"

def reader(path, source_format):
    source_path = qpath(path)
    if source_format == "csv":
        return f"read_csv_auto({{source_path}}, header = true)"
    if source_format == "parquet":
        return f"read_parquet({{source_path}})"
    if source_format == "json_lines":
        return f"read_json_auto({{source_path}}, format = 'newline_delimited')"
    if source_format == "json":
        return f"read_json_auto({{source_path}})"
    raise ValueError(f"Unsupported project source format: {{source_format}}")

con = duckdb.connect(":memory:")
con.execute(f"CREATE SCHEMA IF NOT EXISTS {{qid(schema_name)}}")
for source in config.get("sources") or []:
    source_path = project_root / source["path"]
    if not source_path.is_file():
        raise FileNotFoundError(f"Project source file is missing: {{source_path}}")
    con.execute(
        f"CREATE OR REPLACE VIEW {{qid(schema_name)}}.{{qid(source['view'])}} AS "
        f"SELECT * FROM {{reader(source_path, source['format'])}}"
    )

%sql con --alias project
print(f"Project data ready: {{len(config.get('sources') or [])}} tables loaded.")
'''


